"""CI tests for the real-data-calibrated ``realistic.parquet`` synthetic fixtures.

The fixtures are generated by ``tests/datasets/tracking/{provider}/
generate_synthetic.py`` with ``inject_realistic_edge_cases=True`` and the
provider's empirical baseline. They cover edge cases the strict
deterministic fixtures do not: small fraction of off-pitch player rows,
ball-out interval, ball-x throw-in tail, NaN ball coordinates (when the
provider's baseline shows a non-trivial NaN rate).

These tests verify the adapters handle the edge cases without crashing
or producing schema-violating output, with the same realistic tolerance
the e2e real-data sweep applies (98% in-bounds rather than 100%).
"""

from __future__ import annotations

from pathlib import Path

import pandas as pd
import pytest

from silly_kicks.tracking.schema import (
    PFF_TRACKING_FRAMES_COLUMNS,
    SPORTEC_TRACKING_FRAMES_COLUMNS,
    TRACKING_CONSTRAINTS,
    TrackingConversionReport,
)

_FIX = Path(__file__).resolve().parent / "datasets" / "tracking"


def _bounds_check_loose(frames: pd.DataFrame, provider: str) -> None:
    """98%-bounds check matching the real-data sweep tolerance."""
    for col, (lo, hi) in TRACKING_CONSTRAINTS.items():
        if col not in frames.columns:
            continue
        vals = frames[col].dropna()
        if len(vals) == 0:
            continue
        if hi == float("inf"):
            assert (vals >= lo).all(), f"{provider}/{col}: values below {lo}"
        else:
            in_bounds = vals.between(lo, hi).mean()
            assert in_bounds >= 0.98, f"{provider}/{col}: only {in_bounds:.4f} of values in [{lo}, {hi}]"


def test_sportec_realistic_fixture_round_trips():
    raw = pd.read_parquet(_FIX / "sportec" / "realistic.parquet")
    from silly_kicks.tracking.sportec import convert_to_frames

    frames, report = convert_to_frames(
        raw,
        home_team_id="DFL-CLU-0100",
        home_team_start_left=True,
    )
    assert isinstance(report, TrackingConversionReport)
    assert set(frames.columns) == set(SPORTEC_TRACKING_FRAMES_COLUMNS)
    _bounds_check_loose(frames, "sportec")


def test_sportec_realistic_fixture_has_ball_out_interval():
    raw = pd.read_parquet(_FIX / "sportec" / "realistic.parquet")
