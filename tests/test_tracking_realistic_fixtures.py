def test_sportec_lakehouse_derived_round_trips():
    fixture = _FIX / "sportec" / "lakehouse_derived.parquet"
    if not fixture.exists():
        pytest.skip(f"{fixture} not committed yet --- regenerate via scripts/build_lakehouse_ci_fixtures.py")
    raw = pd.read_parquet(fixture)
    from datasets.tracking._lakehouse_adapter import lakehouse_to_sportec_input

    sportec_input = lakehouse_to_sportec_input(raw).dropna(subset=["x_centered", "y_centered"])
    from silly_kicks.tracking.sportec import convert_to_frames

    home_team_str = str(sportec_input.loc[~sportec_input["is_ball"], "team_id"].dropna().iloc[0])
    frames, report = convert_to_frames(
        sportec_input,
        home_team_id=home_team_str,
        home_team_start_left=True,
    )
    assert isinstance(report, TrackingConversionReport)
    assert set(frames.columns) == set(SPORTEC_TRACKING_FRAMES_COLUMNS)
    _bounds_check_loose(frames, "sportec")


